@asynccontextmanager
async def lifespan(app: FastAPI):
    if PIPELINE_MODE == "live":
        # Ensure schema exists on startup so first GET /api/filings doesn't fail
        try:
            db_cfg = DBConfig(db_path=GOLD_DB_PATH)
            with get_connection(db_cfg) as conn:
                initialize_schema(conn)
        except Exception as exc:
            import logging
            logging.getLogger(__name__).warning("Could not pre-init schema: %s", exc)
    yield


app = FastAPI(
    title="FinTelemetry Compliance API",
    version="2.0.0",
    description=(
        "Gold layer REST API for the SEC EDGAR compliance dashboard. "
        "Switch data source via PIPELINE_MODE=mock|live."
    ),
    lifespan=lifespan,
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["GET", "POST", "OPTIONS"],
    allow_headers=["*"],
)


