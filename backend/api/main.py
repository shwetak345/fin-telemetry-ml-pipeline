@app.get("/api/health")
def health_check() -> dict:
    """Returns server status and active pipeline mode."""
    return {
        "status":        "ok",
        "pipeline_mode": PIPELINE_MODE,
        "db_path":       str(GOLD_DB_PATH) if PIPELINE_MODE == "live" else None,
        "timestamp":     datetime.now(timezone.utc).isoformat(),
    }


@app.get("/api/filings")
def list_filings(limit: int = Query(default=200, ge=1, le=1000)) -> list[dict]:
    """
    Returns a list of shaped filing records.

    In mock mode, limit is applied after generating all 50 mock records.
    In live mode, limit is passed to the DuckDB query.
    """
    return _get_filings(limit)[:limit]


@app.get("/api/filings/{filing_id}")
def get_filing(filing_id: str) -> dict:
    """Returns a single filing record by its id."""
    records = _get_filings()
    match   = next((r for r in records if r["id"] == filing_id), None)
    if not match:
        raise HTTPException(status_code=404, detail=f"Filing '{filing_id}' not found.")
    return match


@app.get("/api/filings/stats/summary")
def filing_stats() -> dict:
    """Aggregate counts for the summary stat cards in the dashboard."""
    records    = _get_filings()
    violations = sum(1 for r in records if r["knownRuleViolation"])
    anomalies  = sum(1 for r in records if r["mlAnomalyFlag"])
    return {
        "total":      len(records),
        "violations": violations,
        "anomalies":  anomalies,
        "healthy":    len(records) - violations - anomalies,
        "mode":       PIPELINE_MODE,
    }


@app.get("/api/pii-audit")
def get_pii_audit_log(limit: int = Query(default=200, ge=1, le=2000)) -> list[dict]:
    """
    Returns PII detection audit log entries from pii_audit_log.

    Each entry records which field, which PII type, how many rows were affected,
    and which pipeline run produced the detection.  Raw PII values are never stored.

    Returns an empty list in mock mode until ingestion_pipeline.py has been run
    at least once (which populates both audit_triage and pii_audit_log).
    """
    db_cfg = DBConfig(db_path=GOLD_DB_PATH, read_only=True)
    try:
        with get_connection(db_cfg) as conn:
            df = conn.execute(
                "SELECT * FROM pii_audit_log ORDER BY detection_ts DESC LIMIT ?",
                [limit],
            ).df()
        return df.to_dict("records")
    except Exception as exc:
        # pii_audit_log doesn't exist yet (no ingestion has run), or DB is
        # unavailable — return empty list rather than a 503 so the dashboard
        # renders cleanly before first pipeline execution.
        if "pii_audit_log" in str(exc) or "does not exist" in str(exc).lower():
            return []
        raise HTTPException(
            status_code=503,
            detail=f"pii_audit_log unavailable: {exc}",
        ) from exc


@app.post("/api/pipeline/run")
def trigger_pipeline(background_tasks: BackgroundTasks) -> dict:
    """
    Triggers a full Bronze → Silver → Gold ingestion run (live mode only).
    The job runs in a background thread; the endpoint returns immediately.
    """
    if PIPELINE_MODE != "live":
        raise HTTPException(
            status_code=400,
            detail="Pipeline trigger is only available in PIPELINE_MODE=live.",
        )
    background_tasks.add_task(_trigger_live_ingestion)
    return {"status": "accepted", "message": "Pipeline triggered. Check /api/health for progress."}


