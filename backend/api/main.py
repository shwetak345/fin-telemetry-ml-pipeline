def _get_mock_filings() -> list[dict]:
    """Returns shaped records from the in-memory mock dataset."""
    raw_rows = get_mock_sec_filings()
    result   = []
    for i, f in enumerate(raw_rows, 1):
        filing_date = f.get("filing_date")
        if hasattr(filing_date, "isoformat"):
            filing_date = filing_date.isoformat()

        row = {
            "id":                   str(i),
            "company_name":         f["company_name"],
            "filing_date":          filing_date,
            "cik":                  f.get("cik"),
            "form_type":            f.get("form_type"),
            "revenue_growth":       None,
            "operating_cash_flow":  float(f["revenue_usd"]) if f.get("revenue_usd") else None,
            "known_rule_violation": f["known_rule_violation"],
            "ml_anomaly_flag":      f["ml_anomaly_flag"],
            "ml_anomaly_score":     f["ml_anomaly_score"],
            "root_cause_message":   f.get("root_cause_message"),
            "created_at":           None,
        }
        result.append(_to_filing_record(row))
    return result


def _get_live_filings(limit: int = 200) -> list[dict]:
    """Queries the Gold DuckDB and returns shaped filing records."""
    db_cfg = DBConfig(db_path=GOLD_DB_PATH, read_only=True)
    try:
        with get_connection(db_cfg) as conn:
            df = fetch_audit_records(conn, limit=limit)
    except Exception as exc:
        raise HTTPException(status_code=503, detail=f"DuckDB unavailable: {exc}") from exc

    return [_to_filing_record(row) for row in df.to_dict("records")]


def _get_filings(limit: int = 200) -> list[dict]:
    return _get_mock_filings() if PIPELINE_MODE == "mock" else _get_live_filings(limit)


# ---------------------------------------------------------------------------
# Pipeline trigger (live mode only)
# ---------------------------------------------------------------------------

def _trigger_live_ingestion() -> dict:
    """
    Runs the full Bronze → Silver → Gold pipeline in a background thread.
    Returns immediately; check /api/health for pipeline status.
    """
    try:
        from backend.live_ingestion.azure_blob_stream import BronzeIngestionConfig, ingest_bronze
        from backend.live_ingestion.pyspark_processor import SilverProcessorConfig, run_silver_pipeline

        bronze_cfg = BronzeIngestionConfig()
        silver_cfg = SilverProcessorConfig(db_path=GOLD_DB_PATH)
        bronze_df  = ingest_bronze(bronze_cfg)
        run_silver_pipeline(bronze_df, silver_cfg)
        return {"status": "completed"}
    except Exception as exc:
        return {"status": "error", "detail": str(exc)}


