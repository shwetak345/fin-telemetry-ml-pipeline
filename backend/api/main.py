_MOCK_ASSIGNEES = [
    "Sarah M.", "James K.", "Emily R.", "David L.",
    "Karen T.", "Michael B.", "Alice C.", "Thomas W.",
]


def _parse_age_token(id_str: str, score: float) -> Optional[str]:
    """Generate a deterministic alert age from id + anomaly score."""
    try:
        id_num = int(hashlib.md5(id_str.encode()).hexdigest(), 16) % 1000
    except Exception:
        id_num = 1
    age_hours = max(1, (id_num * 11 + int(score * 137)) % 193)
    return f"{age_hours}h" if age_hours < 24 else f"{age_hours // 24}d"


def _to_filing_record(row: dict) -> dict:
    """
    Shapes a raw audit_triage row (from DB or mock) into the API response
    object consumed by the React dashboard.  Mirrors the logic in
    frontend/dev_server.js :: toFilingRecord().
    """
    score     = float(row.get("ml_anomaly_score") or 0)
    known_vio = bool(row.get("known_rule_violation"))
    ml_flag   = bool(row.get("ml_anomaly_flag"))
    is_triage = known_vio or ml_flag

    # Cluster classification
    isolation_cluster = (
        "Cluster-7 (High-Risk)"   if score >= 0.8 else
        "Cluster-3 (Medium-Risk)" if score >= 0.5 else
        "Cluster-2 (Low-Risk)"
    )

    # 95% confidence interval
    ci_low  = round(max(0.0, score - 0.04), 2)
    ci_high = round(min(1.0, score + 0.04), 2)

    issue_type = (
        "Rule Violation" if known_vio else
        "ML Anomaly"     if ml_flag   else
        "None"
    )
    severity = (
        "violation" if known_vio else
        "anomaly"   if ml_flag   else
        "healthy"
    )
    status = (
        "Needs Review" if known_vio else
        "In Progress"  if ml_flag   else
        "Resolved"
    )

    id_str = str(row.get("id", "1"))
    try:
        id_num = int(id_str.split("-")[0]) if "-" in id_str else int(id_str)
    except (ValueError, IndexError):
        id_num = abs(hash(id_str)) % 10000

    assignee  = _MOCK_ASSIGNEES[id_num % len(_MOCK_ASSIGNEES)] if is_triage else None
    alert_age = _parse_age_token(id_str, score) if is_triage else None

    raw_ocf = row.get("operating_cash_flow")
    ocf_float = float(raw_ocf) if raw_ocf is not None else 0.0
    revenue_str = f"${ocf_float / 1e9:.1f}B" if raw_ocf is not None else None

    rev_growth  = float(row.get("revenue_growth") or 0)
    cash_ratio  = round(ocf_float / 1e10, 3) if ocf_float != 0 else 0.0

    filing_date = row.get("filing_date")
    if filing_date and hasattr(filing_date, "isoformat"):
        filing_date = filing_date.isoformat()[:10]
    elif filing_date:
        filing_date = str(filing_date)[:10]
    else:
        filing_date = "N/A"

    created_at = row.get("created_at")
    if created_at and hasattr(created_at, "isoformat"):
        ingestion_ts = created_at.isoformat()
    else:
        ingestion_ts = datetime.now(timezone.utc).isoformat()

    return {
        "id":                id_str,
        "company":           str(row.get("company_name", "")),
        "filingDate":        filing_date,
        "issueType":         issue_type,
        "severity":          severity,
        "cik":               str(row.get("cik") or "N/A"),
        "formType":          str(row.get("form_type") or "N/A"),
        "revenue":           revenue_str,
        "knownRuleViolation":known_vio,
        "mlAnomalyFlag":     ml_flag,
        "needsTriage":       is_triage,
        "status":            status,
        "assignee":          assignee,
        "alertAge":          alert_age,
        "violationDetails":  row.get("root_cause_message"),
        "mlTelemetry": {
            "anomalyScore":       score,
            "isolationCluster":   isolation_cluster,
            "ingestionTs":        ingestion_ts,
            "modelVersion":       "IsoForest-v2.4",
            "featureContrib": {
                "revenueGrowthDelta": round(rev_growth, 3),
                "cashFlowRatio":      cash_ratio,
                "filingFrequency":    round(score * 0.3, 3),
            },
            "confidenceInterval": f"{ci_low:.2f} – {ci_high:.2f}",
        },
    }


